package com.bh.b2.ex1;

public class Member {
	
	private String id;
	private int age;
	private int muge;
	private int ki;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getMuge() {
		return muge;
	}
	public void setMuge(int muge) {
		this.muge = muge;
	}
	public int getKi() {
		return ki;
	}
	public void setKi(int ki) {
		this.ki = ki;
	}
	
}
